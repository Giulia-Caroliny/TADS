        u.cadastrarUsuario(1, "Admin", "admin@ifg.edu.br", "123");
        if(txtEmail.getText().equals(u.email) && String.valueOf(txtSenha.getPassword()).equals(u.senha))
        {
            new frmPrincipal().show();
        } else {
            JOptionPane.showMessageDialog(this, "Usuário ou senha inválidos", "Erro", JOptionPane.ERROR_MESSAGE);
            txtEmail.setText("");
            txtSenha.setText("");
        }
