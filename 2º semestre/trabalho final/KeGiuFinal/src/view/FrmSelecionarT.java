    public static void preencherTabelas() {
        DefaultTableModel tabelas = (DefaultTableModel) jtTecidos.getModel();
        tabelas.setNumRows(0);

        listaTecidos = controller.getFiltrarI("'%tecido%'");
        estoque item = new estoque();

        Object colunas[] = new Object[2];

        for (int i = 0; i < listaTecidos.size(); i++) {
            item = listaTecidos.get(i);

            colunas[0] = item.imagem;
            colunas[1] = item.nome;

            tabelas.addRow(colunas);
        }
    }

    private static void Atualizar(int tupla) { //fazer um get nas telas de selecionar e passar para a variÃ¡vel static em encomendas

        encomendaDados.Tecido = listaTecidos.get(tupla).nome;
        encomendaDados.codTecido = listaTecidos.get(tupla).codItem;

        try {
            encomendaDados.imgTecido = listaTecidos.get(tupla).imagem;
        } catch (Exception e) {
            System.out.println(e);
        }
    }
