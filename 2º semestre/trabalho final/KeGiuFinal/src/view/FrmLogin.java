    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        if (controller.Logar(txtLogin.getText(), String.valueOf(txtSenha.getPassword()))) {
            if ("C".equals(UsuarioIdenti.identificador)) {
                controller.getIDCliente();
                new FrmPrincipalC().show();
                this.dispose();
            } else {
                new FrmPrincipalF().show();
                this.dispose();
            }
        } else {
            txtLogin.setText("");
            txtSenha.setText("");
        }

    }//GEN-LAST:event_btnEntrarActionPerformed

