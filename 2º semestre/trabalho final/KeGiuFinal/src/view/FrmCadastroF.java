    private void btnCadasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadasActionPerformed
        Usuario u = new Usuario();
        boolean sair;
        
        u.nome = txtNome.getText();
        u.CPF = Integer.parseInt(txtCPF.getText());
        u.email = txtEmail.getText();
        u.telefone = txtTelefone.getText();
        u.endereco = txtEndereco.getText();
        if ("Cliente".equals(cbxIdenti.getSelectedItem().toString())) {
            u.identificador = "C";
        } else {
            u.identificador = "F";
        }
        u.senha = txtSenha.getText();
        funcionarioCadastro.dataIngresso = txtData.getText();
        funcionarioCadastro.cargo = txtCargo.getText();
        
        sair = controller.inserirUsuario(u);
        if(sair) this.dispose();
    }//GEN-LAST:event_btnCadasActionPerformed

