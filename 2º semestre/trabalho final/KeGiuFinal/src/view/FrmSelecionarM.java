    private static void preencherTabelas (){
        DefaultTableModel tabelas = (DefaultTableModel) jtModelos.getModel();
        tabelas.setNumRows(0);
        
        List <modelos> listaModelo = controller.getUpdateModelos();
                
        Object colunas[] = new Object[2];
                
        for (int i = 0; i < listaModelo.size(); i++) {
                                   
            colunas[0] = listaModelo.get(i).imagem;
            colunas[1] = listaModelo.get(i).molde;
            
            tabelas.addRow(colunas);
        }
        listaModelos = listaModelo;
    }
    
    private static void preencherTabelasFiltro (String filtro){
        DefaultTableModel tabelas = (DefaultTableModel) jtModelos.getModel();
        tabelas.setNumRows(0);
        
        List <modelos> listaModelo = controller.getFiltrarModelos(filtro);
                
        Object colunas[] = new Object[2];
                
        for (int i = 0; i < listaModelo.size(); i++) {
                                   
            colunas[0] = listaModelo.get(i).imagem;
            colunas[1] = listaModelo.get(i).molde;
            
            tabelas.addRow(colunas);
        }
        
        listaModelos = listaModelo;
    }
    
    private static void Atualizar(){ //fazer um get nas telas de selecionar e passar para a variável static em encomendas
        encomendaDados.Modelo = listaModelos.get(num).molde;
        encomendaDados.codItens = listaModelos.get(num).codMateriais;
        
        try {
            encomendaDados.imgModelo = listaModelos.get(num).imagem;            
        } catch (Exception e) {
            System.out.println(e);
        }
    }
    
