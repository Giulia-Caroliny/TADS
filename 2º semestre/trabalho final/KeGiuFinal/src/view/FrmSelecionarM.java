    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (jtModelos.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um modelo.");
        } else {
            num = jtModelos.getSelectedRow();
            Atualizar();
            FrmEncomendar.AtualizarModelo();
            if (encomendaDados.Modelo != null && encomendaDados.Tecido != null) {
                FrmEncomendar.CalcularValor();
            }
            this.dispose();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

